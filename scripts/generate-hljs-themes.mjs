import {readdirSync, readFileSync, statSync, writeFileSync} from "node:fs";
import {basename, dirname, join, relative, resolve, sep} from "node:path";

const root = process.cwd();
const stylesDir = join(root, "node_modules", "highlight.js", "styles");
const outFile = join(root, "src", "markdown", "generatedHljsThemes.ts");

const MIME_TYPES = new Map([
  [".jpg", "image/jpeg"],
  [".jpeg", "image/jpeg"],
  [".png", "image/png"],
  [".gif", "image/gif"],
  [".webp", "image/webp"],
  [".svg", "image/svg+xml"],
]);

const NAME_OVERRIDES = new Map([
  ["1c-light", "1C Light"],
  ["a11y-dark", "A11y Dark"],
  ["a11y-light", "A11y Light"],
  ["androidstudio", "Android Studio"],
  ["atom-one-dark", "Atom One Dark"],
  ["atom-one-dark-reasonable", "Atom One Dark Reasonable"],
  ["atom-one-light", "Atom One Light"],
  ["github", "GitHub Light"],
  ["github-dark", "GitHub Dark"],
  ["github-dark-dimmed", "GitHub Dark Dimmed"],
  ["googlecode", "Google Code"],
  ["ir-black", "IR Black"],
  ["isbl-editor-dark", "ISBL Editor Dark"],
  ["isbl-editor-light", "ISBL Editor Light"],
  ["nnfx-dark", "NNFX Dark"],
  ["nnfx-light", "NNFX Light"],
  ["qtcreator-dark", "Qt Creator Dark"],
  ["qtcreator-light", "Qt Creator Light"],
  ["stackoverflow-dark", "Stack Overflow Dark"],
  ["stackoverflow-light", "Stack Overflow Light"],
  ["vs", "VS"],
  ["vs2015", "VS2015"],
  ["xcode", "Xcode"],
]);

function listCssFiles(dir) {
  return readdirSync(dir)
    .flatMap((entry) => {
      const full = join(dir, entry);
      if (statSync(full).isDirectory()) {
        return listCssFiles(full);
      }
      return entry.endsWith(".css") && !entry.endsWith(".min.css") ? [full] : [];
    })
    .sort((a, b) => themeIdFromPath(a).localeCompare(themeIdFromPath(b)));
}

function themeIdFromPath(file) {
  return relative(stylesDir, file).split(sep).join("/").replace(/\.css$/, "");
}

function titleCase(id) {
  return id
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((part) => {
      if (/^\d/.test(part)) return part.toUpperCase();
      return part.charAt(0).toUpperCase() + part.slice(1);
    })
    .join(" ");
}

function themeName(id) {
  if (id.startsWith("base16/")) {
    const shortId = id.slice("base16/".length);
    return `Base16 / ${NAME_OVERRIDES.get(shortId) ?? titleCase(shortId)}`;
  }
  return NAME_OVERRIDES.get(id) ?? titleCase(id);
}

function mimeTypeFor(file) {
  const ext = file.slice(file.lastIndexOf(".")).toLowerCase();
  return MIME_TYPES.get(ext) ?? "application/octet-stream";
}

function inlineRelativeAssets(css, file) {
  return css.replace(/url\((['"]?)(?!data:|https?:|#)([^'")]+)\1\)/g, (match, _quote, rawUrl) => {
    const assetPath = resolve(dirname(file), rawUrl);
    try {
      const bytes = readFileSync(assetPath);
      return `url(data:${mimeTypeFor(assetPath)};base64,${bytes.toString("base64")})`;
    } catch {
      return match;
    }
  });
}

const themes = listCssFiles(stylesDir)
  .map((file) => {
    const id = themeIdFromPath(file);
    const sourcePath = relative(stylesDir, file).split(sep).join("/");
    return {
      id,
      name: themeName(id),
      group: id.startsWith("base16/") ? "Base16" : "Highlight.js",
      sourcePath,
      css: inlineRelativeAssets(readFileSync(file, "utf8"), file).replace(/\r\n/g, "\n"),
    };
  })
  .sort((a, b) => {
    if (a.id === "vs2015") return -1;
    if (b.id === "vs2015") return 1;
    if (a.group !== b.group) return a.group === "Highlight.js" ? -1 : 1;
    return a.name.localeCompare(b.name);
  });

const body = `// Generated by scripts/generate-hljs-themes.mjs. Do not edit by hand.
export interface GeneratedHljsTheme {
  id: string;
  name: string;
  group: "Highlight.js" | "Base16";
  sourcePath: string;
  css: string;
}

export const GENERATED_HLJS_THEMES: GeneratedHljsTheme[] = ${JSON.stringify(themes, null, 2)};
`;

writeFileSync(outFile, body, "utf8");
console.log(`Generated ${themes.length} highlight.js themes -> ${outFile}`);
